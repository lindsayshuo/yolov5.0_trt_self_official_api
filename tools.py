def get_frame_batch_step(batch_size, img_dir, step=4):
    ret = []
    batch = []
    cap = cv2.VideoCapture(img_dir)
    idx = 0
    while True:
        _, frame = cap.read()
        if frame is None:
            break
        if idx % step == 0:
            if len(batch) == batch_size:
                ret.append(batch)
                batch = []
            batch.append(frame)
        idx += 1
    if len(batch) > 0:
        ret.append(batch)
    size = (int(cap.get(cv2.CAP_PROP_FRAME_WIDTH)),
            int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT)))
    cap.release()
    cv2.destroyAllWindows()
    # print(type(ret))
    # print(type(ret[0]))
    # print(type(ret[0][0]))
    print(img_dir + " load")
    return ret, size
